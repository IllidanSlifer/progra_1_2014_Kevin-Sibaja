             public String letra(char pletra)
             {
                  String letra;
                  
                
                 
                 switch(pletra)
                 {
                     case 'a':
                        return "excelente";
                         
                     case 'b':
                         return "bueno";
                     case 'c':
                         return "regular"; 
                         
                     case 'd':
                         return "malo"; 
                         
                     case 'e':
                         return "pesimo";      
                         
                     default:
                         break;
                         
                 }
                 return pletra;
             }        
             
                    
                 
                
